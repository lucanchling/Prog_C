// Pour la multiplication
int multiplier(int nb1, int nb2) {
    return nb1*nb2;
}

// Pour l'addition
int additionner(int nb1, int nb2) {
    return nb1+nb2;
}

// Pour la soustraction
int soustraire(int nb1, int nb2) {
    return nb1-nb2;
}

// etc...
float diviser(int nb1, int nb2) {
    return nb1/nb2;
}

int modulo(int nb1, int nb2) {
    return nb1%nb2;
}

/* Fichier: opérateurs.c
* permet d'appréhender les différents opérateurs avec deux variables
* auteur: Luc Anchling
*/

#include <stdio.h> // headers

int main() {
int a = 16;
int b = 3;
printf("a+b=%d\n",a+b);
printf("a-b=%d\n",a-b);
printf("a*b=%d\n",a*b);
printf("a/b=%d\n",a/b);
printf("a%%b=%d\n",a%b);
return 0;
}
